def main():
    parser = argparse.ArgumentParser(
        description="Summarize token usage from safety_analysis.jsonl files or .eval files",
    )
    parser.add_argument(
        "path",
        type=str,
        help="Path to safety_analysis.jsonl file, .eval file, or run directory",
    )
    parser.add_argument(
        "--detailed",
        action="store_true",
        help="Show per-record breakdown (only for .jsonl files)",
    )
    parser.add_argument(
        "--csv",
        action="store_true",
        help="Output in CSV format",
    )

    args = parser.parse_args()

    path = Path(args.path)

    if not path.exists():
        print(f"Error: Path not found: {path}", file=sys.stderr)
        return 1

    # Handle directory input (auto-detect source)
    if path.is_dir():
        # Try to use token_stats_generator for unified handling
        try:
            from token_stats_generator import (
                generate_token_summary,
                print_summary as generator_print_summary,
            )
            stats = generate_token_summary(path)
            if not stats:
                print(f"Error: No token stats found in directory: {path}", file=sys.stderr)
                return 1

            if args.csv:
                print_csv_summary({
                    "total_input_tokens": stats.get("total_input_tokens", 0),
                    "total_output_tokens": stats.get("total_output_tokens", 0),
                    "total_tokens": stats.get("total_tokens", 0),
                    "total_cache_read": stats.get("total_cache_read", 0),
                    "total_cache_write": stats.get("total_cache_write", 0),
                    "total_reasoning": stats.get("total_reasoning", 0),
                    "records_with_token_stats": stats.get("records_with_token_stats", stats.get("total_records", 0)),
                    "total_records": stats.get("total_records", 0),
                    "step1_count": stats.get("step1_count", 0),
                    "step1_input_tokens": stats.get("step1_input_tokens", 0),
                    "step1_output_tokens": stats.get("step1_output_tokens", 0),
                    "step2_count": stats.get("step2_count", 0),
                    "step2_input_tokens": stats.get("step2_input_tokens", 0),
                    "step2_output_tokens": stats.get("step2_output_tokens", 0),
                    "step3_count": stats.get("step3_count", 0),
                    "step3_input_tokens": stats.get("step3_input_tokens", 0),
                    "step3_output_tokens": stats.get("step3_output_tokens", 0),
                    "step4_count": stats.get("step4_count", 0),
                    "step4_input_tokens": stats.get("step4_input_tokens", 0),
                    "step4_output_tokens": stats.get("step4_output_tokens", 0),
                    "context_analysis_count": stats.get("context_analysis_count", 0),
                    "context_analysis_input_tokens": stats.get("context_analysis_input_tokens", 0),
                    "context_analysis_output_tokens": stats.get("context_analysis_output_tokens", 0),
                    "rewriting_count": stats.get("rewriting_count", 0),
                    "rewriting_input_tokens": stats.get("rewriting_input_tokens", 0),
                    "rewriting_output_tokens": stats.get("rewriting_output_tokens", 0),
                })
            else:
                generator_print_summary(stats)
            return 0
        except ImportError:
            print("Error: token_stats_generator not available for directory mode", file=sys.stderr)
            return 1

    # Handle .jsonl file input (original behavior)
    if path.suffix == ".jsonl":
        records = load_jsonl(path)

        if not records:
            print("Error: No records found in file", file=sys.stderr)
            return 1

        totals = aggregate_token_stats(records)

        if args.csv:
            print_csv_summary(totals)
        elif args.detailed:
            print_detailed_summary(totals, records)
        else:
            print_summary(totals)
        return 0

    # Handle .eval file input
    if path.suffix == ".eval":
        try:
            from token_stats_generator import (
                generate_token_stats_from_eval,
                print_summary as generator_print_summary,
            )
            stats = generate_token_stats_from_eval(path)
            if not stats:
                print(f"Error: No token stats found in .eval file: {path}", file=sys.stderr)
                return 1

            if args.csv:
                print_csv_summary({
                    "total_input_tokens": stats.get("total_input_tokens", 0),
                    "total_output_tokens": stats.get("total_output_tokens", 0),
                    "total_tokens": stats.get("total_tokens", 0),
                    "total_cache_read": stats.get("total_cache_read", 0),
                    "total_cache_write": stats.get("total_cache_write", 0),
                    "total_reasoning": stats.get("total_reasoning", 0),
                    "records_with_token_stats": 0,
                    "total_records": stats.get("total_records", 0),
                    "step1_count": 0,
                    "step1_input_tokens": 0,
                    "step1_output_tokens": 0,
                    "step2_count": 0,
                    "step2_input_tokens": 0,
                    "step2_output_tokens": 0,
                    "step3_count": 0,
                    "step3_input_tokens": 0,
                    "step3_output_tokens": 0,
                    "step4_count": 0,
                    "step4_input_tokens": 0,
                    "step4_output_tokens": 0,
                    "context_analysis_count": 0,
                    "context_analysis_input_tokens": 0,
                    "context_analysis_output_tokens": 0,
                    "rewriting_count": 0,
                    "rewriting_input_tokens": 0,
                    "rewriting_output_tokens": 0,
                })
            else:
                generator_print_summary(stats)
            return 0
        except ImportError:
            print("Error: token_stats_generator not available for .eval file mode", file=sys.stderr)
            return 1

    # Unknown file type
    print(f"Error: Unsupported file type: {path.suffix}", file=sys.stderr)
    print("Expected: .jsonl file, .eval file, or directory", file=sys.stderr)
    return 1


if __name__ == "__main__":
    sys.exit(main())
